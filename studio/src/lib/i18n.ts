function readStoredLocaleMode(): StudioLocaleMode {
  if (typeof window === 'undefined') return 'auto';
  const value = window.localStorage.getItem(STORAGE_KEY);
  return value === 'en' || value === 'zh-CN' || value === 'auto' ? value : 'auto';
}

function detectBrowserLocale(): StudioLocale {
  if (typeof navigator === 'undefined') return 'en';
  const candidates = [...(navigator.languages ?? []), navigator.language].filter(Boolean);
  return candidates.some((item) => item.toLowerCase().startsWith('zh')) ? 'zh-CN' : 'en';
}

function resolveLocale(mode: StudioLocaleMode): StudioLocale {
  return mode === 'auto' ? detectBrowserLocale() : mode;
}

function lookupMessage(locale: StudioLocale, key: string): string | undefined {
  return key.split('.').reduce<string | MessageTree | undefined>((current, part) => {
    if (!current || typeof current === 'string') return undefined;
    return current[part];
  }, messages[locale]) as string | undefined;
}

function formatMessage(template: string, params?: Record<string, string | number | boolean | null | undefined>): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, token) => String(params[token] ?? ''));
}

const localeMode = ref<StudioLocaleMode>(readStoredLocaleMode());
const locale = ref<StudioLocale>(resolveLocale(localeMode.value));

function syncDocumentLanguage(nextLocale: StudioLocale): void {
  if (typeof document !== 'undefined') {
    document.documentElement.lang = nextLocale;
  }
}

syncDocumentLanguage(locale.value);

function refreshAutoLocale(): void {
  if (localeMode.value !== 'auto') return;
  locale.value = detectBrowserLocale();
  syncDocumentLanguage(locale.value);
}

if (typeof window !== 'undefined') {
  window.addEventListener('languagechange', refreshAutoLocale);
}

export function setStudioLocale(nextLocale: StudioLocaleMode): void {
  localeMode.value = nextLocale;
  locale.value = resolveLocale(nextLocale);
  if (typeof window !== 'undefined') {
    window.localStorage.setItem(STORAGE_KEY, nextLocale);
  }
  syncDocumentLanguage(locale.value);
}

export function useStudioI18n() {
  const currentLocale = computed(() => locale.value);
  const currentLocaleMode = computed(() => localeMode.value);

  function t(key: string, params?: Record<string, string | number | boolean | null | undefined>): string {
    const template = lookupMessage(locale.value, key) ?? lookupMessage('en', key) ?? key;
    return formatMessage(template, params);
  }

  return {
    locale: currentLocale,
    localeMode: currentLocaleMode,
    t,
    setLocale: setStudioLocale,
  };
}
